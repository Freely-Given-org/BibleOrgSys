def translateGerman( htmlStr:str ) -> str:
    """
    Do basic word-for-word translation from Luther's German Bible to English.

    Text in htmlStr parameter can be inside a span,
        e.g., '<span class="Wycl_verseTextChunk">ech man in his seruice, and in the offring,</span>'
    """
    fnPrint( DEBUGGING_THIS_MODULE, f"translateGerman( ({len(htmlStr)}) )" )

    # if html.startswith( 'und '): # Handle common exception that can't be expressed in the word table
    #     html = f'and {html[4:]}'
    htmlStr = htmlStr.replace( '»', '‘' ).replace( '«', '’' )
    for GermanWord,EnglishWord in GERMAN_WORD_MAP:
        htmlStr = htmlStr.replace( GermanWord, EnglishWord )
        if GermanWord[0] == ' ':
            if htmlStr.startswith( GermanWord[1:] ):
                htmlStr = htmlStr.replace( GermanWord[1:], EnglishWord[1:], 1 )
            htmlStr = htmlStr.replace( f'>{GermanWord[1:]}', f'>{EnglishWord[1:]}' )

    return ( htmlStr.replace('j','y').replace('J','Y') # Try to write it like it might have sounded
                # But then need to fix up some regular English words
                .replace('conyur','conjur')
                .replace('deyect','deject')
                .replace('yoy','joy')
                .replace('eyoice','ejoice')
                .replace('yaguar','jaguar')
                .replace('yail','jail')
                .replace('yudg','judg')
                .replace('yust','just')
                .replace('(ay)','(aj)')
            )
