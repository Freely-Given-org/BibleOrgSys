for wordMapEntry in LATIN_WORD_MAP:
    assert len(wordMapEntry) == 2, f"{wordMapEntry}"
    LatinWord,EnglishWord = wordMapEntry
    assert isinstance( LatinWord, str ) and len(LatinWord)>=2, f"{LatinWord=}"
    assert LatinWord != EnglishWord, f"Attempting to replace identical Latin word: {LatinWord=}"
    assert LatinWord not in LatinWords, f"duplicate LatinWord: {LatinWord=} ({EnglishWord=})"
    if LatinWord.startswith(' ') or EnglishWord.startswith(' '): assert LatinWord.startswith(' '), f"Mismatched leading space: {LatinWord=} {EnglishWord=}"
    if LatinWord.endswith(' ') or EnglishWord.endswith(' '): assert LatinWord.endswith(' '), f"Mismatched trailing space: {LatinWord=} {EnglishWord=}"
    # assert LatinWord not in EnglishWord, f"Recursive substitution of '{LatinWord}' into '{EnglishWord}'"
    assert '  ' not in LatinWord
    LatinWords.append( LatinWord)
    if LatinWord.startswith(' '): assert EnglishWord.startswith(' '), f"Mismatched leading space:  {LatinWord=} {EnglishWord=}"
    if LatinWord.endswith(' '): assert EnglishWord.endswith(' '), f"Mismatched trailing space: {LatinWord=} {EnglishWord=}"
    if EnglishWord[-1] in ' ,.:;':
        assert LatinWord[-1] == EnglishWord[-1], f"Mismatched trailing character: {LatinWord=} {EnglishWord=}"
    if LatinWord[-1] in ' ,.:;':
        assert EnglishWord[-1] == LatinWord[-1], f"Mismatched trailing character: {LatinWord=} {EnglishWord=}"
    assert '  ' not in EnglishWord
    if ' ' not in LatinWord.strip():
        assert ' ' not in EnglishWord.strip(), f"Space in middle of {EnglishWord=}" # No space in middle
    EnglishWords.append( EnglishWord )
del LatinWords, EnglishWords

def translateLatin( htmlStr:str ) -> str:
    """
    Do basic word-for-word translation from Latin Vulgate Bible to English.

    Text in htmlStr parameter can be inside a span,
        e.g., '<span class="Wycl_verseTextChunk">ech man in his seruice, and in the offring,</span>'
    """
    fnPrint( DEBUGGING_THIS_MODULE, f"translateLatin( ({len(htmlStr)}) )" )

    # NOTE: Didn't work because it's already html, so added back to table above with '>'
    # if html.startswith( 'et '): # Handle common exception that can't be expressed in the word table
    #     html = f'and {html[3:]}'
    # elif html.startswith( 'qui '):
    #     html = f'who {html[4:]}'
    for LatinWord,EnglishWord in LATIN_WORD_MAP:
        htmlStr = htmlStr.replace( LatinWord, EnglishWord )
        if LatinWord[0] == ' ':
            htmlStr = htmlStr.replace( f'>{LatinWord[1:]}', f'>{EnglishWord[1:]}' )


    return ( htmlStr.replace('j','y').replace('J','Y') # Try to write it like it might have sounded
                # But then need to fix up some regular English words
                .replace('yealous','jealous').replace('yourney','journey')
                .replace('yubilation','jubilation')
                .replace('Yudge','Judge').replace('yudge','judge')
                .replace('Yust ','Just ').replace('yust','just')
                .replace('Yhes','Jhes') # Change these ones back again -- 'Jhesus' -- was maybe more like French J ???
                .replace('mayest','majest').replace('proyect','project').replace('reyect','reject').replace('reyoic','rejoic')
            ) 
